<h1>Seething Shore</h1>
<p>Azerothot szörnyű csapás érte, sebei mélyek, és egy erős új alkotó eleme szivárgott a felszínre. 
Ahogy az ellenségeskedés a Horde és az Alliance között újra kirobbant, 
mindkét oldal felismerte hogy hogyan fordítsák az Azeritben rejlő potenciált saját javukra az elkövetkezendő harcokban. 
