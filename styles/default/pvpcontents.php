Ez teszi lehetővé azt, hogy bár a sziget kicsi, mégis változatos. Mindekőzben a harctérre küldemények érkeznek, melyek felerősítéseket (buffokat) tartalmaznak. 
Ezeket fel kell venned, ahhoz hogy erősebb legyél ellengfeleidnél és dominálni tudj a végsőkig a harcban.
